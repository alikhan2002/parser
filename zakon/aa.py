# Разбиваем текст на части
parts = text.split('\n\n')
print(parts)

# Создаем пустой словарь для хранения данных
data = {}

# Проходимся по каждой части и извлекаем информацию
for part in parts:
    if part.strip() != '':
        # Разделяем часть на название и содержание
        title, content = part.split('\n', 1)

        # Извлекаем номер раздела и название статьи (если есть)
        section_number, article_title = '', ''
        if 'РАЗДЕЛ' in title:
            section_number, article_title = title.split(' ', 2)[1], ''
        elif 'Статья' in title:
            section_number, article_title = '', title

        # Добавляем информацию в словарь
        data.setdefault('parts', []).append({
            'section_number': section_number.strip(),
            'article_title': article_title.strip(),
            'content': content.strip()
        })

# Преобразуем данные в JSON формат и выводим
json_data = json.dumps(data, ensure_ascii=False, indent=4)
print(json_data)
